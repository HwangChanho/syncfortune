// app/src/lib/gyeokguk.ts — 핵심 격(格, 동적 구조) 검출
// ─────────────────────────────────────────────────────────────────────────
// daniel: 만세력에 해당 사주의 *핵심 격*(살인상생·식신제살·상관패인 등)을 보여준다.
//   = 기본 격(정관격/재격 — 월령·투출)과 별개로, 십신끼리 생/제/화하는 *동적 구조*.
// ★격 판정 규칙·우선순위 = daniel 명리 stance 검수 슬롯(§3.3). 지금은 천간+지지본기 십신 *존재* 기반 후보 검출.
//   엔진이 후보를 내고, 어느 게 진짜 핵심인지·조건 정밀화는 daniel이 판정/보강한다(예: 강약·투출·월령 가중).
// ─────────────────────────────────────────────────────────────────────────

export type Gyeokguk = { name: string; hanja: string; desc: string };

/**
 * 핵심 격 후보 검출.
 * @param present 천간 십신 + 지지 본기 십신의 집합(10정밀: 비견·겁재·식신·상관·정재·편재·정관·편관·정인·편인).
 *                일간 자신(비견)은 구조 판정에 무관하므로 패턴 조건에서 자연히 빠진다.
 * @returns 검출된 격 구조 배열(없으면 빈 배열). 보통 1~3개 — 표시 후 daniel이 핵심 1개로 정제.
 */
export function detectGyeokguk(present: Set<string>): Gyeokguk[] {
  const has = (k: string) => present.has(k);
  const 인성 = has('정인') || has('편인');     // 印 — 배움·수용·뒷배경
  const 재성 = has('정재') || has('편재');     // 財 — 재물·욕심·이성
  const 칠살 = has('편관');                    // 七殺 — 누르는 압박·도전
  const out: Gyeokguk[] = [];

  // ① 칠살(편관)을 무엇으로 다스리나 — 압박을 어떻게 푸느냐가 구조의 핵심
  if (칠살) {
    if (인성) out.push({ name: '살인상생', hanja: '殺印相生', desc: '누르는 압박(칠살)을 배움·수용(인성)이 받아 나를 키우는 힘으로 돌리는 구조.' });
    if (has('식신')) out.push({ name: '식신제살', hanja: '食神制殺', desc: '실력·표현(식신)으로 압박(칠살)을 눌러 다스리는 구조.' });
    if (재성) out.push({ name: '재생살', hanja: '財生殺', desc: '재물(재성)이 압박(칠살)을 키우는 구조 — 재물·욕심을 직접 좇을수록 압박이 세질 수 있음.' });
  }

  // ② 상관(날카로운 표현)을 무엇으로 다스리나
  if (has('상관')) {
    if (인성) out.push({ name: '상관패인', hanja: '傷官佩印', desc: '배움(인성)이 날카로운 표현(상관)을 다스려 균형을 잡는 구조.' });
    else if (재성) out.push({ name: '상관생재', hanja: '傷官生財', desc: '재능·표현(상관)이 재물(재성)로 이어지는 구조.' });
  }

  // ③ 식신생재 — 꾸준한 생산이 재물로
  if (has('식신') && 재성) out.push({ name: '식신생재', hanja: '食神生財', desc: '꾸준한 생산·재능(식신)이 재물(재성)로 이어지는 구조.' });

  // ④ 관인상생 — 직책·책임이 배움·인정으로
  if (has('정관') && 인성) out.push({ name: '관인상생', hanja: '官印相生', desc: '직책·책임(정관)이 배움·인정(인성)으로 이어져 성장하는 구조.' });

  // ⑤ 재생관 — 재물이 명예·지위를 뒷받침
  if (재성 && has('정관')) out.push({ name: '재생관', hanja: '財生官', desc: '재물(재성)이 명예·지위(정관)를 뒷받침하는 구조.' });

  return out;
}
