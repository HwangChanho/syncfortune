// daniel: 만세력에 해당 사주의 *핵심 격*(살인상생·식신제살·상관패인 등)을 보여준다.
//   = 기본 격(정관격/재격 — 월령·투출)과 별개로, 십신끼리 생/제/화하는 *동적 구조*.
// ★격 판정 규칙·우선순위 = daniel 명리 stance 검수 슬롯(§3.3).
//
// ── B5(daniel 2026-07-06): 격 발화 조건을 '월령 중심'으로 게이트 ──
//   [문제] 이전엔 천간 + 지지 본기 십신의 *단순 공존*(Set.has)만으로 격이 성립 → 성립 불가한 격이 남발.
//          예: 년(年)에 편관 + 시(時)에 인성 → 월령과 무관한데도 '살인상생'이 떠 버림.
//   [수정] 자평(子平) 표준 = 격은 월령(月令) 중심. 격은 그 *주기(主氣)*가
//          ① 월지 본기(월령)이거나  ② 월지 지장간이 천간에 투출(透出)했을 때만 성립.
//          → 주기(격을 세우는 핵심 십신)를 '월령 앵커 집합(wollyeong)'으로 게이트하고,
//            상신(相神, 격을 돕는 십신)만 원국 어디든 *존재(present)*하면 된다.
//   ⚠️ 더 느슨하게 풀지 않는다(daniel: 자평 표준 월령 중심 유지). 주기 배정의 정밀화 = daniel stance 슬롯.
// ─────────────────────────────────────────────────────────────────────────

export type Gyeokguk = { name: string; hanja: string; desc: string };

/**
 * 핵심 격 후보 검출.
 * @param present   원국 전체의 천간 십신 + 지지 본기 십신 집합 — *상신(相神, 격을 돕는 십신)* 존재 확인용.
 *                  (10정밀: 비견·겁재·식신·상관·정재·편재·정관·편관·정인·편인). 일간 자신(비견)은 조건에서 자연 배제.
 * @param wollyeong 월령 앵커 십신 집합 = { 월지 본기 십신 } ∪ { 월지 지장간 중 천간에 투출한 것의 십신 }.
 *                  B5 게이트: 격의 *주기(主氣)*는 반드시 이 집합에 있어야 성립(자평 월령 중심).
 * @returns 검출된 격 구조 배열(없으면 빈 배열). 월령 게이트로 보통 0~2개 — 표시 후 daniel이 핵심 1개로 정제.
 */
export function detectGyeokguk(present: Set<string>, wollyeong: Set<string>): Gyeokguk[] {
  const has = (k: string) => present.has(k);            // 상신(相神): 원국 어디든 존재하면 OK
  const anchored = (k: string) => wollyeong.has(k);     // 주기(主氣): 월령(본기 or 투출)에 앵커됐나 — B5 게이트
  const 인성 = has('정인') || has('편인');     // 印 — 배움·수용·뒷배경 (상신 후보)
  const 재성 = has('정재') || has('편재');     // 財 — 재물·욕심·이성 (상신 후보)
  const out: Gyeokguk[] = [];

  // ① 칠살격(七殺格) — 주기 '편관'이 월령 앵커일 때만 성립. 그 압박을 무엇으로 다스리나(상신)가 구조의 핵심.
  if (anchored('편관')) {
    if (인성) out.push({ name: '살인상생', hanja: '殺印相生', desc: '누르는 압박(칠살)을 배움·수용(인성)이 받아 나를 키우는 힘으로 돌리는 구조.' });
    if (has('식신')) out.push({ name: '식신제살', hanja: '食神制殺', desc: '실력·표현(식신)으로 압박(칠살)을 눌러 다스리는 구조.' });
    if (재성) out.push({ name: '재생살', hanja: '財生殺', desc: '재물(재성)이 압박(칠살)을 키우는 구조 — 재물·욕심을 직접 좇을수록 압박이 세질 수 있음.' });
  }

  // ② 상관격(傷官格) — 주기 '상관'이 월령 앵커일 때만. 날카로운 표현을 무엇으로 다스리나.
  if (anchored('상관')) {
    if (인성) out.push({ name: '상관패인', hanja: '傷官佩印', desc: '배움(인성)이 날카로운 표현(상관)을 다스려 균형을 잡는 구조.' });
    else if (재성) out.push({ name: '상관생재', hanja: '傷官生財', desc: '재능·표현(상관)이 재물(재성)로 이어지는 구조.' });
  }

  // ③ 식신격(食神格) — 주기 '식신'이 월령 앵커 + 재성(상신) → 식신생재
  if (anchored('식신') && 재성) out.push({ name: '식신생재', hanja: '食神生財', desc: '꾸준한 생산·재능(식신)이 재물(재성)로 이어지는 구조.' });

  // ④ 정관격(正官格) — 주기 '정관'이 월령 앵커 + 인성(상신) → 관인상생
  if (anchored('정관') && 인성) out.push({ name: '관인상생', hanja: '官印相生', desc: '직책·책임(정관)이 배움·인정(인성)으로 이어져 성장하는 구조.' });

  // ⑤ 재격(財格) — 주기 '재성'(정재 or 편재)이 월령 앵커 + 정관(상신) → 재생관
  if ((anchored('정재') || anchored('편재')) && has('정관')) out.push({ name: '재생관', hanja: '財生官', desc: '재물(재성)이 명예·지위(정관)를 뒷받침하는 구조.' });

  return out;
}
