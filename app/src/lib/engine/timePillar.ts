// app/src/lib/timePillar.ts — 시주 역추론(TPR) 후보 생성 (무료·온디바이스·결정론, API 0)
// ─────────────────────────────────────────────────────────────────────────
// 기획: time_pillar_reconstruction_spec.md (시주를 모르는 사용자에게 인생 사건을 입력받아
//   12개 시주 후보를 정합성 순으로 좁히는 결정론 + 자미 교차 모듈).
//
// 이 파일 = MVP 1단계(§14): **오서둔(五鼠遁) 12후보 생성기**. 순수 결정론(동일 입력→동일 출력).
//   핵심 명리 사실: 일간(日干)이 고정되면 오서둔으로 子시 천간이 결정되고, 12지지 각각의
//   시주가 천간 순행으로 정해진다 → 탐색공간은 60갑자가 아니라 **지지 12후보**뿐이다(§5 Step1).
//
//   ※ 스코어링(§7 격국·자녀궁·앵커 정합)·증거 입력 UX·자미 교차는 후속 단계(명리 stance=daniel★).
//   ※ 야자시/조자시(§9.3)·경계 보정(§9.4)은 birth_clock 이 있을 때의 후속 고도화.
// ─────────────────────────────────────────────────────────────────────────
import type { Stem, Branch } from '@spec/chart';

// 천간·지지 순서 — 표준 60갑자 기준(engine/sinsal.ts 등과 동일값, 모듈 로컬 정의 관행).
const STEMS: Stem[] = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸'];
const BRANCHES: Branch[] = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥'];

// 五鼠遁(오서둔) — 일간 → 子시 천간. 甲己→甲 / 乙庚→丙 / 丙辛→戊 / 丁壬→庚 / 戊癸→壬.
//   (전통 오서둔표 — 甲己일은 甲子시부터, 乙庚일은 丙子시부터 … 순으로 시작하는 둔간 규칙)
const ZI_STEM_BY_DAY: Record<Stem, Stem> = {
  甲: '甲', 己: '甲',
  乙: '丙', 庚: '丙',
  丙: '戊', 辛: '戊',
  丁: '庚', 壬: '庚',
  戊: '壬', 癸: '壬',
};

// 각 시지의 시각 구간(진태양시 기준 2시간 블록, 子=23~01). 사용자 표시용.
const HOUR_RANGE: Record<Branch, string> = {
  子: '23:00–01:00', 丑: '01:00–03:00', 寅: '03:00–05:00', 卯: '05:00–07:00',
  辰: '07:00–09:00', 巳: '09:00–11:00', 午: '11:00–13:00', 未: '13:00–15:00',
  申: '15:00–17:00', 酉: '17:00–19:00', 戌: '19:00–21:00', 亥: '21:00–23:00',
};

/** 시주 후보 1개 — 12지지 각각에 오서둔으로 결정된 시간(時干) + 표시용 시각 구간. */
export type TimePillarCandidate = {
  branch: Branch;    // 시지(時支) — 12후보의 식별자(子~亥)
  stem: Stem;        // 시간(時干) — 오서둔으로 결정
  hourRange: string; // 해당 시지의 시각 구간(예 '17:00–19:00')
};

/**
 * 오서둔 12시주 후보 생성 — 일간(日干)만으로 결정되는 결정론 함수.
 *
 * 子시 천간을 오서둔으로 정하고, 12지지를 순행하며 천간도 함께 순행시킨다(10간 순환).
 * 예) 辛 일간 → 子=戊子, 丑=己丑, 寅=庚寅, 卯=辛卯, 辰=壬辰, 巳=癸巳, 午=甲午,
 *     未=乙未, 申=丙申, 酉=丁酉, 戌=戊戌, 亥=己亥 (기획서 §5 Step1 예시와 일치).
 *
 * @param dayStem 일간(日干)
 * @returns 子~亥 순서의 12개 시주 후보
 */
export function timePillarCandidates(dayStem: Stem): TimePillarCandidate[] {
  const ziStem = ZI_STEM_BY_DAY[dayStem];   // 子시 천간(오서둔)
  const ziIdx = STEMS.indexOf(ziStem);
  return BRANCHES.map((branch, i) => ({
    branch,
    stem: STEMS[(ziIdx + i) % 10],          // 지지 순행에 맞춰 천간도 순행
    hourRange: HOUR_RANGE[branch],
  }));
}
