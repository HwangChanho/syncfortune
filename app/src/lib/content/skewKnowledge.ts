/**
 * 십성 쏠림 → 문제/대응(또는 강점/살리는 법). 만세력 '쏠림' 시트가 소비.
 *  1) 지배 십신군(5, 합계). 과다(합 ≥ 4) 아니면 null(쏠림 없음).
 *  2) 그 군 안에서 최강 개별 십신(정/편·식/상 분리) 선택.
 *  3) 신강약 게이트(R44 3분기): 재성·관성 + **신강** → STRONG_FAVOR_SKEW(용신·길) / 중화·신약 → TENGOD_SKEW(병).
 * @param detail analyzeTenGods(saju).detail — 10정밀 십신 개수
 * @param verdict scoreStrength(saju).verdict — '신강' | '중화' | '신약'
 */
export function tengodSkew(detail: Record<string, number>, verdict: '신강' | '중화' | '신약'): TengodSkewResult | null {
  const groupSum: Record<G5, number> = { 비겁: 0, 식상: 0, 재성: 0, 관성: 0, 인성: 0 };
  const byGroup: Record<G5, { god: TenGod; n: number }[]> = { 비겁: [], 식상: [], 재성: [], 관성: [], 인성: [] };
  for (const [k, v] of Object.entries(detail)) {
    const n = v as number;
    if (!(n > 0)) continue;
    const g = TO5[k as TenGod];
    if (!g) continue;
    groupSum[g] += n;
    byGroup[g].push({ god: k as TenGod, n });
  }
  // 1) 지배 군 + 과다(≥4) 게이트 — 기존 컷(consumer 의 domTg[1] >= 4) 유지
  //   ※ for...of 직접 재대입(forEach 클로저 아님) — group 을 G5 로 유지해 아래 재/관 비교 narrowing 방지.
  let group: G5 = '비겁', maxG = -1;
  for (const g of (Object.keys(groupSum) as G5[])) { if (groupSum[g] > maxG) { maxG = groupSum[g]; group = g; } }
  if (maxG < 4) return null;
  // 2) 군 내 최강 개별 십신(동률이면 먼저 담긴 순 — detail 나열 순)
  const cands = byGroup[group];
  if (cands.length === 0) return null;
  let god = cands[0].god, maxN = cands[0].n;
  for (const c of cands) if (c.n > maxN) { maxN = c.n; god = c.god; }
  // 3) 신강약 게이트: 재/관 + 신강 → 용신(길) 반전
  const isJaeGwan = group === '재성' || group === '관성';
  const favorable = isJaeGwan && verdict === '신강' && !!STRONG_FAVOR_SKEW[god];
  const item = favorable ? STRONG_FAVOR_SKEW[god]! : TENGOD_SKEW[god];
  return { god, group, item, favorable };
}
