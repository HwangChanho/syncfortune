/**
 * 생년월일 숫자열 → YYYY-MM-DD 자동 하이픈.
 *   예: "19900315" → "1990-03-15", "1990031" → "1990-03-1"(편집 중 허용).
 *   숫자 외 문자는 제거하고 최대 8자리(YYYYMMDD)만 사용 → 사용자가 지우며 편집 가능.
 */
export function formatBirthDate(raw: string): string {
  const d = raw.replace(/\D/g, '').slice(0, 8); // 숫자만, 최대 8자리
  const y = d.slice(0, 4);
  const m = d.slice(4, 6);
  const day = d.slice(6, 8);
  return [y, m, day].filter((s) => s.length > 0).join('-');
}
